import React from "react";
import { createBrowserRouter, RouterProvider } from "react-router-dom";
import { Root, Home, Plan, About } from "./routes";

const router = createBrowserRouter([
  {
    path: "/",
    element: <Root />,
    children: [
      {
        index: true,
        element: <Home />,
      },
      {
        path: "plan",
        element: <Plan />,
      },
      {
        path: "about",
        element: <About />,
      },
    ],
  },
]);

const App = () => {
  return <RouterProvider router={router} />;
};

export default App;
